# Tuplas
# son inmutables (no pueden cambiar)

nombres = ('Pepe', 'Pedro', 'Pablo')

# Mostrar elementos de la tupla
print(nombres)

# Acceder a un elemento en especifico
print(nombres[0])

# Longitud de la tupla
print(len(nombres))

# no se puede cambiar el valor en una tupla
