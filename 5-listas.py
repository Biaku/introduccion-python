# Listas
# https://docs.python.org/3/tutorial/datastructures.html#list-comprehensions

numeros = [10, 15, 20]

print(numeros)

# agregar elemento al final de  lista
numeros.append(25)
print(numeros)

# agregar elemento en X posicion
numeros.insert(0, 5)
print(numeros)

# eliminar elemento
numeros.remove(25)
print(numeros)

# eliminar elemento por indice
numeros.pop(0)
print(numeros)
