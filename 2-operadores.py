# Operadores aritméticos comunes

# +	Suma
# -	Resta
# *	Multiplicación
# /	División


numero1 = 10
numero2 = 5

# + Suma
print("Suma")
print(numero1 + numero2)

# -	Resta
print("Resta")
print(numero1 - numero2)

# *	Multiplicación
print("Multiplicacion")
print(numero1 * numero2)

# /	División
print("Division")
print(numero1 / numero2)
